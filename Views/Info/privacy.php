  <h2>プライバシーポリシー</h2>
  Starport.comは、社会に寄与する企業として個人情報を適切に取り扱うことの重要性を認識し、個人情報保護方針を定め、役員および従業員が一体となってこれを遵守し、もって個人情報の保護および個人の権利利益の保護に万全を尽くしてまいります。
  <ol>
   	<li>個人情報を取得するにあたっては、その利用目的をできる限り特定し、その目的の達成に必要な限度において個人情報を取得致します。</li>
   	<li>個人情報を、本人から直接、書面によって取得する場合には、com名、個人情報保護管理者および連絡先、利用目的等をお知らせした上で、必要な範囲で個人情報を取得致します。</li>
   	<li>個人情報の利用は、本人が同意を与えた利用目的の範囲内で行います。また、目的外利用を行わないため、必要な対策を講じる手順を確立し、実施致します。</li>
   	<li>保有する個人情報を適切な方法で管理し、警察からの要請や利用規約違反の判明等必要措置の場合を除き、本人の同意なしに第三者に開示・提供致しません。</li>
   	<li>保有する個人情報を利用目的に応じた必要な範囲内において、正確、かつ、最新の状態で管理し、個人情報の漏洩、滅失または毀損等のおそれに対して、合理的な安全対策を講じ、予防並びに是正に努めます。</li>
   	<li>個人情報の処理を外部へ委託する場合は、漏洩や第三者への提供を行わないこと等を契約により義務づけ、委託先に対する適切な管理を実施致します。</li>
   	<li>保有する個人情報についての苦情・相談は、弊社の問合せ窓口に連絡頂くことにより、これに対応致します。</li>
   	<li>個人情報の取扱いに関する法令、国が定める指針その他の規範を遵守致します。</li>
   	<li>個人情報保護マネジメントシステムを定め、これを定期的に見直し、継続的に改善致します。</li>
  </ol>
